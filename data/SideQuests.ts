export const createInitialSideQuestState = (): SideQuestProgressState => ({
  cases: SIDE_QUEST_CASES.reduce<Record<string, SideQuestCaseProgress>>((cases, sideQuestCase) => {
    cases[sideQuestCase.id] = createEmptyCaseProgress();
    return cases;
  }, {}),
  masterRewardAwarded: false,
});

export const normalizeSideQuestState = (state?: Partial<SideQuestProgressState> | null): SideQuestProgressState => {
  const normalized = createInitialSideQuestState();

  SIDE_QUEST_CASES.forEach((sideQuestCase) => {
    const savedCase = state?.cases?.[sideQuestCase.id];
    if (!savedCase) return;

    const foundClueIds = Array.from(new Set(savedCase.foundClueIds || []))
      .filter((clueId) => sideQuestCase.clues.some((clue) => clue.id === clueId));
    const allCluesFound = foundClueIds.length === sideQuestCase.clues.length;
    const savedStatus = savedCase.status || 'notStarted';
    const status = savedStatus === 'active' && allCluesFound
      ? 'readyToSolve'
      : savedStatus;

    normalized.cases[sideQuestCase.id] = {
      status,
      foundClueIds,
      rewardAwarded: !!savedCase.rewardAwarded || savedStatus === 'completed',
      completedAt: savedCase.completedAt,
    };
  });

  normalized.masterRewardAwarded = !!state?.masterRewardAwarded;
  return normalized;
};
